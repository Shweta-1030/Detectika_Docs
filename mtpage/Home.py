import streamlit as st

st.title('About Storage')
st.header('Cloud storage:')
st.markdown("""
Cloud storage is a model of computer data storage in which the digital data is stored in logical pools, said to be on "the cloud". The physical storage spans
multiple servers (sometimes in multiple locations), and the physical environment is typically owned and managed by a hosting company. These cloud storage
providers are responsible for keeping the data available and accessible, and the physical environment secured, protected, and running. People and organizations 
buy or lease storage capacity from the providers to store user, organization, or application data.Cloud storage services may be accessed through a colocated 
cloud computing service, a web service application programming interface (API) or by applications that use the API, such as cloud desktop storage,a cloud storage 
gateway or Web-based content management systems.
""")

st.header('Data storage:')
st.markdown("""
Data storage is the recording (storing) of information (data) in a storage medium. Handwriting, phonographic recording, magnetic tape, and optical discs are 
all examples of storage media. Biological molecules such as RNA and DNA are considered by some as data storage. Recording may be accomplished with virtually 
any form of energy. Electronic data storage requires electrical power to store and retrieve data.Data storage in a digital, machine-readable medium is 
sometimes called digital data. Computer data storage is one of the core functions of a general-purpose computer. Electronic documents can be stored in much
less space than paper documents. Barcodes and magnetic ink character recognition (MICR) are two ways of recording machine-readable data on paper.
""")
